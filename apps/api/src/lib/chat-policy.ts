export type ChatPolicyMode = 'knowledge' | 'general' | 'blocked';

export type ChatPolicyDecision = {
  mode: ChatPolicyMode;
  reason?: string;
};

type ChatPolicyInput = {
  prompt: string;
  hasKnowledgeScope: boolean;
};

const SYSTEM_LEVEL_PATTERNS = [
  /\b(systemctl|service|journalctl|chmod|chown|sudo|bash|powershell|cmd\.exe|terminal|shell)\b/i,
  /\b(rm\s+-rf|del\s+\/f|shutdown|reboot|restart service|kill process|taskkill)\b/i,
  /(\u91cd\u542f\u670d\u52a1|\u67e5\u770b\u670d\u52a1\u5668\u65e5\u5fd7|\u7cfb\u7edf\u547d\u4ee4|\u6267\u884c\u547d\u4ee4|\u5b89\u88c5\u8f6f\u4ef6|\u5378\u8f7d\u8f6f\u4ef6|\u4fee\u6539\u73af\u5883\u53d8\u91cf|\u67e5\u770b\u73af\u5883\u53d8\u91cf|\u90e8\u7f72\u5230\u670d\u52a1\u5668|\u62c9\u53d6\u4ee3\u7801|\u91cd\u7f6e\u7cfb\u7edf)/i,
];

const LOCAL_WRITE_VERB_PATTERN =
  /(\u5199\u5165|\u6539\u5199|\u8986\u76d6|\u5220\u9664|\u91cd\u547d\u540d|\u4fdd\u5b58|\u843d\u76d8|\u66f4\u65b0\u914d\u7f6e|\u4fee\u6539\u914d\u7f6e|\u4fee\u6539|\u66f4\u65b0|\u5199\u6587\u4ef6|\u6539\u6587\u4ef6|\u5220\u6587\u4ef6|\u8ffd\u52a0\u5230\u6587\u4ef6)/i;

const LOCAL_WRITE_TARGET_PATTERN =
  /(\u77e5\u8bc6\u5e93|\u6587\u6863\u4e2d\u5fc3|\u914d\u7f6e\u6587\u4ef6|\u672c\u5730\u6587\u4ef6|\u670d\u52a1\u5668\u6587\u4ef6|\u62a5\u8868\u8bb0\u5f55|\u5386\u53f2\u6570\u636e|\u73af\u5883\u53d8\u91cf|\u7cfb\u7edf\u914d\u7f6e)/i;

const MEMORY_REQUEST_PATTERNS = [
  /(\u8bf7)?\u8bb0\u4f4f/i,
  /\u8bb0\u4e00\u4e0b/i,
  /\u8bb0\u4e0b\u6765/i,
  /\u4ee5\u540e.*(\u6309|\u7528|\u9075\u5faa)/i,
  /\u9ed8\u8ba4.*(\u6309|\u7528|\u9075\u5faa)/i,
  /\u6211\u7684.*(\u504f\u597d|\u8981\u6c42|\u98ce\u683c|\u89c4\u5219)/i,
  /\u6211\u53eb/i,
  /\u6211\u662f/i,
  /remember\s+(this|that|my|me)/i,
  /save\s+(this|that|my)\s+(preference|preferences|profile|context|memory)/i,
  /from now on/i,
  /default to/i,
];

function looksLikeLocalWriteRequest(prompt: string) {
  return LOCAL_WRITE_VERB_PATTERN.test(prompt) && LOCAL_WRITE_TARGET_PATTERN.test(prompt);
}

function looksLikeSystemLevelRequest(prompt: string) {
  return SYSTEM_LEVEL_PATTERNS.some((pattern) => pattern.test(prompt));
}

export function looksLikeMemoryPreferenceRequest(prompt: string) {
  return MEMORY_REQUEST_PATTERNS.some((pattern) => pattern.test(String(prompt || '').trim()));
}

export function classifyChatPrompt(input: ChatPolicyInput): ChatPolicyDecision {
  const prompt = String(input.prompt || '').trim();

  if (looksLikeSystemLevelRequest(prompt)) {
    return {
      mode: 'blocked',
      reason: '\u5f53\u524d\u9875\u9762\u4e0d\u652f\u6301\u7cfb\u7edf\u7ea7\u6307\u4ee4\u3001\u670d\u52a1\u63a7\u5236\u6216\u670d\u52a1\u5668\u8fd0\u7ef4\u64cd\u4f5c\u3002',
    };
  }

  if (looksLikeLocalWriteRequest(prompt)) {
    return {
      mode: 'blocked',
      reason: '\u5f53\u524d\u9875\u9762\u4e0d\u652f\u6301\u76f4\u63a5\u6539\u5199\u672c\u5730\u5df2\u6709\u6570\u636e\u3001\u77e5\u8bc6\u5e93\u5185\u5bb9\u6216\u7cfb\u7edf\u914d\u7f6e\u3002',
    };
  }

  if (input.hasKnowledgeScope) {
    return { mode: 'knowledge' };
  }

  return { mode: 'general' };
}

